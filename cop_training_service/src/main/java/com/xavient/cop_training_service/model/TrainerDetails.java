package com.xavient.cop_training_service.model;

public class TrainerDetails {

	private String trainerName;
	private String trainerPrefrence;
	
	public String getTrainerName() {
		return trainerName;
	}
	public void setTrainerName(String trainerName) {
		this.trainerName = trainerName;
	}
	public String getTrainerPrefrence() {
		return trainerPrefrence;
	}
	public void setTrainerPrefrence(String trainerPrefrence) {
		this.trainerPrefrence = trainerPrefrence;
	}
	
	
}
